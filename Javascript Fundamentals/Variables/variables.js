// Variable is named storage for data
// To create a variable use the "let" keyword

let message; // This declares a variable to which a value can be assigned

message = 'Hello'; // Store the string in the variable

// Can combine declaraction and assignment into a single statement

let writing = 'Hello'
// Do not declare a variable twice this will create an error

// Naming:
// Must contain only letters, digits, or the symbols '$' and '_'
// First character must not be a digit

// Case sensitive 'apple' not = 'APPLE'

// Constants are variables whose values can not be changed. 
// Use 'const' instead of 'let' when declaring constants

const myBirthday = '.05.06.2005'